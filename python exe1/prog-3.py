#Write a program that accept two numbers and perform all basic mathematical operation and print.

no1=int(input('enter no1: '))
no2=int(input('enter no2: '))

print(no1, "+", no2, " = ", no1+no2)
print(no1, "-", no2, " = ", no1-no2)
print(no1, "*", no2, " = ", no1*no2)
print(no1, "**", no2, " = ", no1**no2)
print(no1, "/", no2, " = ", no1/no2)
print(no1, "//", no2, " = ", no1//no2)
print(no1, "%", no2, " = ", no1%no2)
