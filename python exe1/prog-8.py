#Write a program to find volume of cylinder.

radius=float(input("enter radius of cylinder in cemi: "))
height=float(input("enter height of cylinder in cemi: "))

volume=3.14*(radius*radius)*height

print("Radius of Cylinder is: {0} \nHeight of cylinder is: {1} \n Volume of cylinder become: {2}".format(height, radius, volume))
